function generateRules(a, b) {
  const rules = [];
  const tkiA=a.tki, tkiB=b.tki;
  if (a.mbti[0]!==b.mbti[0]) {
    const intro = a.mbti[0]==='I'?a:b, extro = a.mbti[0]==='E'?a:b;
    rules.push(`${extro.short}: дай ${intro.short} паузу перед ответом. ${intro.short}: скажи «мне нужно время» вслух — иначе ${extro.short} додумает за тебя`);
  } else if (a.mbti[0]==='E') {
    rules.push(`Оба экстраверты — риск перебивать друг друга. Правило: один говорит, второй слушает 2 мин. Потом — меняемся`);
  }
  if (tkiA.compete>=8 && tkiB.compete>=8) {
    rules.push(`Два бойца: договоритесь о формате спора ДО того, как начнёте. «3 минуты каждому, потом ищем общее»`);
  } else if (tkiA.compete>=8 && tkiB.collaborate>=7) {
    const fighter=tkiA.compete>=8?a:b, collab=tkiA.compete>=8?b:a;
    rules.push(`${fighter.short}: снижай напор на 20% — ${collab.short} и так ищет общее решение. ${collab.short}: не соглашайся слишком быстро ради мира`);
  } else if ((tkiA.avoid>=6 && tkiB.compete>=7) || (tkiB.avoid>=6 && tkiA.compete>=7)) {
    const avoider = tkiA.avoid>=6?a:b, fighter = tkiA.avoid>=6?b:a;
    rules.push(`${avoider.short}: скажи «вернусь через час с ответом» вместо ухода. ${fighter.short}: дай ${avoider.short} время — это не сдача, а обработка`);
  } else {
    rules.push(`Оба умеренны в конфликте — следите, чтобы несогласие не уходило «под ковёр». Спрашивайте: «ты правда согласен?»`);
  }
  const hardFeedback = [a,b].filter(p=>p.tki.collaborate<=4 || p.tki.accommodate>=7);
  if (hardFeedback.length > 0) {
    rules.push(`ОС друг другу: наедине, конкретно, с примером. ${hardFeedback.map(p=>p.short).join(' и ')} — сложнее давать/принимать ОС, нужен безопасный формат`);
  } else {
    rules.push(`ОС друг другу: формат «ситуация → поведение → влияние». Обоим важна конкретика, не абстракции`);
  }
  return rules;
}

